package basicpackage;

public class IfElse {


    public static void main(String[] args) {
        int b = 20;

        if (b == 10) {
            System.out.println("B value is 10");
        } else {
            System.out.println("B value is 20");
        }

        do {
            System.out.println(b);
            b++;

        }
        while (b<=30);

    }
}
